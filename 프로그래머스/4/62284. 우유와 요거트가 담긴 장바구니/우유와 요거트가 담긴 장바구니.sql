WITH V AS (
    SELECT DISTINCT CART_ID FROM CART_PRODUCTS WHERE NAME = 'Yogurt'
)
,
T AS (
    SELECT DISTINCT CART_ID FROM CART_PRODUCTS WHERE NAME = 'Milk'
)


SELECT V.CART_ID FROM V JOIN T ON V.CART_ID = T.CART_ID ORDER BY V.CART_ID;





